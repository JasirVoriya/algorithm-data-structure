//d 6 5
#include<stdio.h>
int main(void)
{
    int a,b,c,d;
    for(a=1;a<3;a++)
    {
        b=6-a;
        for(c=1;c<3;c++)
        {
            d=7-c;
            if(a!=c&&b!=d)
            {
                if(a==1){
                    puts("L");
                    break;
                }
                if(c==1){
                    puts("W");
                    break;
                }
            }
        }
    }
}